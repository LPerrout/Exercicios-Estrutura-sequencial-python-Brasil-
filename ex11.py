#Faça um Programa que peça 2 números inteiros e um número real. Calcule e mostre:...
n1 = int(input("Digite o primeiro numero inteiro: "))
n2 = int(input("Digite o segundo numero inteiro: "))
n3 = float(input("Digite um numero real: "))
print(f"o produto do dobro do primeiro com metade do segundo: {(2*n1)*(n2/2)}")
print(f"a soma do triplo do primeiro com o terceiro: {3*n1+n3}")
print(f"o terceiro elevado ao cubo: {n3**3}")